package src.dadopessoas;

public class Person {
    double height;
    String sex;

    public Person() {
        this.sex = sex;
        this.height = height;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
